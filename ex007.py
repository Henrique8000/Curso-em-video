# Programa para calcular a média de duas notas
# Entrada de dados / declaração de variáveis
nota1 = float(input('Digite a primeira nota: '))
nota2 = float(input('Digite a segunda nota: '))

# Declaração da variável de processamento
media = (nota1 + nota2) / 2

# Saída de dados
print(f'A média entre {nota1:.1f} e {nota2:.1f} é igual a {media:.1f}')

