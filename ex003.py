# Soma entre dois números inteiros

# declaração de variáveis / Entrada de dados
n1 = int(input('Digite o primeiro número: '))
n2 = int(input('Digite o segundo número: '))

# Processamento de dados / soma dos números
soma = n1 + n2

# Saída de dados / resultado
print(f'A soma entre {n1} e {n2} vale: {soma}. O tipo primitivo é {type(soma)}')


